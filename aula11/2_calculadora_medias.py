# Cálculo da média aritmética
numeros1 = [8, 9, 7]
media1 = sum(numeros1) / len(numeros1)

numeros2 = [4, 5, 6]
media2 = sum(numeros2) / len(numeros2)

# Soma das duas médias
soma_medias = media1 + media2

# Média das médias
media_medias = (media1 + media2) / 2

# Impressão dos resultados
print("Média do conjunto [8, 9, 7]:", media1)
print("Média do conjunto [4, 5, 6]:", media2)
print("Soma das duas médias:", soma_medias)
print("Média das médias:", media_medias)
